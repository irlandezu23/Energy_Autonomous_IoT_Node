static void USER_AT_send(uint8_t * payload)
{
	uint8_t response[MAX_RESP_LEN];
	do
	{
		HAL_UART_Transmit(&huart4,payload,strlen(payload),TIMEOUT);
		HAL_UART_Receive(&huart4,response,MAX_RESP_LEN,TIMEOUT);
	}while(NULL == strstr(response,ok));
}

static void USER_check_njs(void)
{
	uint8_t response[MAX_RESP_LEN];
		do
		{
			HAL_UART_Transmit(&huart4,njs,strlen(njs),TIMEOUT);
			HAL_UART_Receive(&huart4,response,MAX_RESP_LEN,TIMEOUT);
			if(AT_TRUE == response[0])
			{
				break;
			}
			USER_AT_send(join);
			HAL_UART_Receive(&huart4,response,MAX_RESP_LEN,WAIT_TIME);
			if(NULL != strstr(response,"JOINED"))
			{
				break;
			}
		}while(1);
}

static void USER_get_device_eui(void)
{
	uint8_t response[MAX_RESP_LEN];
	uint8_t * eui;

	do
	{
		HAL_UART_Transmit(&huart4,device_eui_prefix,strlen(device_eui_prefix),TIMEOUT);
		HAL_UART_Receive(&huart4,response,MAX_RESP_LEN,TIMEOUT);
	}while(NULL == strstr(response,ok));
	eui = strtok(response,"\r\n");
	HAL_UART_Transmit(&huart2,device_eui_prefix,strlen(device_eui_prefix)-1,TIMEOUT);
	HAL_UART_Transmit(&huart2,eui,strlen(eui),TIMEOUT);

}

static void USER_enter_sleep_mode(void)
{
	  HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,GPIO_PIN_RESET);
	  HAL_TIM_Base_Start_IT(&htim6);
	  SysTick->CTRL &= ~SysTick_CTRL_ENABLE_Msk;
	  HAL_PWR_EnterSLEEPMode(PWR_MAINREGULATOR_ON, PWR_SLEEPENTRY_WFI);
	  SysTick->CTRL |= SysTick_CTRL_ENABLE_Msk;
	  HAL_TIM_Base_Stop_IT(&htim6);
	  HAL_GPIO_WritePin(GPIOA,GPIO_PIN_5,GPIO_PIN_SET);
}

